window.getQueryObj = function(){
  let url = decodeURI(document.location.toString());
  let queryString = url.split('?')[1] || []
  let queryObj = {};
  if (queryString[0]) {
    queryString = queryString.split('&');
  }
  queryString.forEach(query => {
    let data = query.split('=');
    if (data[0]) {
      queryObj[data[0]] = data[1] || null;
    }
  });
  return queryObj;
}